package Character;

public class Stats {
    
    public int HLT;
    public int ATK;
    public int DEF;
    public int SPD;
    public int MAG;

    public Stats(int HLT, int ATK, int DEF, int SPD, int MAG){
        this.HLT = HLT;
        this.ATK = ATK;
        this.DEF = DEF;
        this.SPD = SPD;
        this.MAG = MAG;
    }
}
